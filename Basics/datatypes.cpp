
#include <iostream>
using namespace std;

int main() {
    // Integer (whole number) data type, typically 4 bytes
    int integerType;

    // Character data type, 1 byte
    char charType;

    // Floating point data type, typically 4 bytes
    float floatType;

    // Double floating point data type, typically 8 bytes
    double doubleType;

    // Boolean data type, 1 byte
    bool boolType;

    // Wide character type, typically 2 or 4 bytes
    wchar_t wideCharType;

    // Short integer, typically 2 bytes
    short shortType;

    // Long integer, typically 4 bytes
    long longType;

    // Long long integer, typically 8 bytes
    long long longLongType;

    // Unsigned versions of integer types. Same size as their signed counterparts.
    unsigned int unsignedIntType;
    unsigned char unsignedCharType;
    unsigned short unsignedShortType;
    unsigned long unsignedLongType;
    unsigned long long unsignedLongLongType;

    return 0;
}